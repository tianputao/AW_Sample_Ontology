def parse_products(raw):
    out = []
    for line in raw.strip().splitlines():
        cols = line.split("|")
        out.append({
            "ProductID":         int(cols[0]),
            "Name":              cols[1],
            "ProductNumber":     cols[2],
            "Color":             cols[3] or None,
            "StandardCost":      float(cols[4]),
            "ListPrice":         float(cols[5]),
            "Size":              cols[6] or None,
            "Weight":            float(cols[7]) if cols[7] else None,
            "ProductCategoryID": int(cols[8]),
        })
    return out

def parse_order_lines(raw):
    out = []
    for line in raw.strip().splitlines():
        cols = line.split("|")
        out.append({
            "SalesOrderID":      int(cols[0]),
            "OrderQty":          int(cols[1]),
            "ProductID":         int(cols[2]),
            "UnitPrice":         float(cols[3]),
            "UnitPriceDiscount": float(cols[4]),
            "LineTotal":         float(cols[5]),
        })
    return out

